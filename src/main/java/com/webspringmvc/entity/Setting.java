package com.webspringmvc.entity;

public class Setting {
	private String pathLogo;
	private String pathLogoHotel;
	private String nameHotel;
	private String nameWeb;
	private String ho;
	private String ten;
	private String phai;
	private String diaChi;
	private String sdt;
	private String email;
	
	public Setting() {
		
	}
	
	public String getPathLogoHotel() {
		return pathLogoHotel;
	}

	public void setPathLogoHotel(String pathLogoHotel) {
		this.pathLogoHotel = pathLogoHotel;
	}

	public String getPathLogo() {
		return pathLogo;
	}
	public void setPathLogo(String pathLogo) {
		this.pathLogo = pathLogo;
	}
	public String getNameHotel() {
		return nameHotel;
	}
	public void setNameHotel(String nameHotel) {
		this.nameHotel = nameHotel;
	}
	public String getNameWeb() {
		return nameWeb;
	}
	public void setNameWeb(String nameWeb) {
		this.nameWeb = nameWeb;
	}

	public String getHo() {
		return ho;
	}

	public void setHo(String ho) {
		this.ho = ho;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}

	public String getPhai() {
		return phai;
	}

	public void setPhai(String phai) {
		this.phai = phai;
	}

	public String getDiaChi() {
		return diaChi;
	}

	public void setDiaChi(String diaChi) {
		this.diaChi = diaChi;
	}

	public String getSdt() {
		return sdt;
	}

	public void setSdt(String sdt) {
		this.sdt = sdt;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}
